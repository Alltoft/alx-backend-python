#!/usr/bin/env python3
"""Implement a type-annotated function to_str that takes a float n as argument
and returns the string representation of the float."""


def to_str(n: float) -> str:
    """
    Convert a float to a string representation.

    Args:
        n (float): The float number to be converted.

    Returns:
        str: The string representation of the input float.

    """
    return f'{n}'
